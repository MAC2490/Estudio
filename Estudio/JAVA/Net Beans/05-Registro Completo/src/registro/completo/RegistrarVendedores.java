    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        String documento = ingresar_documento.getText();
        String nombre = ingresar_nombre.getText();
        String apellido = ingresar_apellido.getText();
        String telefono = ingresar_telefono.getText();
        String direccion = ingresar_direccion.getText();
        String email = ingresar_email.getText();

        if (!documento.equals("") && !nombre.equals("") && !apellido.equals("") && !telefono.equals("") && !direccion.equals("") && !email.equals("")){
            Personas temp = new Personas(documento, nombre, apellido, telefono, direccion, email);
            this.ventanaMenu.listaVendedores[this.ventanaMenu.contVendedores] = temp;
            this.ventanaMenu.contVendedores++;
            dispose();
            this.ventanaMenu.setVisible(true);
        }
    }//GEN-LAST:event_jButton2ActionPerformed

